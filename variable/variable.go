package main

import "fmt"

func main() {
	var a = "text 1"
	fmt.Println(a)

	var b, c = 5, 8
	fmt.Println(b, c) // print multiple variable

	var d, e, f = "faisal", "Alam", 30 // declare multiple variable at once
	fmt.Println(d)
	fmt.Println(e)
	fmt.Println(f)

	var g int = 50 // variable type declard
	fmt.Println(g)

	var h int
	fmt.Println(h) // initial value set 0

	x := "apple" // sort form of  (var x string = "apple")
	fmt.Println(x)

}
